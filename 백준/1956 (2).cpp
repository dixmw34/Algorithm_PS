int main(void) {

	ios::sync_with_stdio(false);
	cin.tie(NULL); cout.tie(NULL);

	int n, e, a, b, c;

	cin >> n >> e;
	int arr[401][401];

	for (int i = 1; i <= n; i++) {
		for (int j = 1; j <= n; j++) {
			arr[i][j] = INF;
		}
	}

	while (e--) {
		cin >> a >> b >> c;
		arr[a][b] = c;
	}

	for (int k = 1; k <= n; k++) {
		for (int i = 1; i <= n; i++) {
			for (int j = 1; j <= n; j++) {
				if (arr[i][j] > arr[i][k] + arr[k][j])arr[i][j] = arr[i][k] + arr[k][j];
			}
		}
	}

	int ans = INF;
	for (int i = 1; i <= n; i++)ans = min(ans, arr[i][i]);


	if (ans == INF)cout << "-1\n";
	else cout << ans << '\n';
	

	return 0;

}